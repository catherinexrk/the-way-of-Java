public class demo1 {
    public static void main(String[] args) {
        System.out.println(-10 / 3);
        System.out.println(10 / -3);
        System.out.println(10 % 3);
        System.out.println(-10 % 3);
        System.out.println(10 % -3);
//      a % b = a - a / b * b
        int i =10;
        System.out.println(i);
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
    }
}
