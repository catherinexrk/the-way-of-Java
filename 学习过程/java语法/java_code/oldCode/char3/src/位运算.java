public class 位运算 {
    public static void main(String[] args) {

        /*
        & 按位与
        | 按位或
        ^ 按位异或


        >> 算术右移
        << 算术左移

         */
        System.out.println(2&3);//2

        System.out.println(2|3);//3

        System.out.println(~-2);//1

        System.out.println(~2);//-3

        System.out.println(1 >> 2);//0
        System.out.println(1 << 2);//4




    }
}
